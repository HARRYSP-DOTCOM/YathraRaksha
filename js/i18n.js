  init(onChange) {
    this._onChange = onChange;
    const saved = localStorage.getItem(this.STORAGE_KEY) || "en";
    const select = document.getElementById("lang-select");
    if (select) {
      select.value = this.LANGUAGES[saved] ? saved : "en";
      select.addEventListener("change", (e) => this.setLang(e.target.value, true));
    }
    this.setLang(select?.value || saved, false);
  },

  setLang(code, notify) {
    if (!this.LANGUAGES[code]) code = "en";
    this.currentLang = code;
    localStorage.setItem(this.STORAGE_KEY, code);
    document.documentElement.lang = this.LANGUAGES[code].html;
    const select = document.getElementById("lang-select");
    if (select && select.value !== code) select.value = code;
    this.apply();
    if (notify) {
      if (typeof this._onChange === "function") this._onChange(code);
      if (window.App?.showToast) {
        window.App.showToast(`${this.t("lang_changed")} ${this.LANGUAGES[code].native}`);
      }
    }
  },

  t(key) {
    const lang = this.strings[this.currentLang] || {};
    const en = this.strings.en || {};
    return lang[key] ?? en[key] ?? key;
  },

  getLang() {
    return this.currentLang;
  },

  getLangName() {
    return this.LANGUAGES[this.currentLang]?.label || "English";
  },

  getSpeechLang() {
    return this.LANGUAGES[this.currentLang]?.speech || "en-IN";
  },

